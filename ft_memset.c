#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned	long	i;
	char				*d;

	i = 0;
	d = b;
	while (i < len)
	{
		*(d + i) = c;
		i++;
	}
	return (b);
}
